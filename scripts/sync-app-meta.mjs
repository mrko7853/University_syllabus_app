import { execSync } from 'node:child_process';
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, '..');
const packageJsonPath = path.join(projectRoot, 'package.json');
const outputPath = path.join(projectRoot, 'js', 'app-meta.js');

function readGitValue(command) {
  try {
    return execSync(command, {
      cwd: projectRoot,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore']
    }).trim();
  } catch (_) {
    return '';
  }
}

function inferMode() {
  const explicitMode = String(process.env.APP_MODE || process.env.NODE_ENV || '').trim();
  if (explicitMode) return explicitMode;
  const lifecycle = String(process.env.npm_lifecycle_event || '').toLowerCase();
  if (lifecycle.includes('build') || lifecycle.includes('release')) return 'production';
  return 'development';
}

async function main() {
  const packageJsonRaw = await fs.readFile(packageJsonPath, 'utf8');
  const packageJson = JSON.parse(packageJsonRaw);
  const mode = inferMode();
  const buildLabel = String(
    process.env.VITE_APP_BUILD_LABEL
    || process.env.APP_BUILD_LABEL
    || (mode === 'production' ? 'production' : 'development')
  ).trim();
  const version = String(packageJson.version || '0.0.0').trim() || '0.0.0';
  const builtAt = new Date().toISOString();
  const builtAtCompact = builtAt.replace(/[-:.TZ]/g, '').slice(0, 14);
  const commit = readGitValue('git rev-parse --short HEAD') || 'unknown';
  const branch = readGitValue('git rev-parse --abbrev-ref HEAD') || 'unknown';
  const dirty = Boolean(readGitValue('git status --porcelain'));
  const buildId = `${buildLabel}-${builtAtCompact}-${commit}${dirty ? '-dirty' : ''}`;

  const appMeta = {
    appName: 'ILA Companion',
    version,
    build: buildLabel,
    mode,
    buildId,
    commit,
    branch,
    dirty,
    builtAt
  };

  const source = `// This file is auto-generated by scripts/sync-app-meta.mjs
// Do not edit manually.
export const APP_META = Object.freeze(${JSON.stringify(appMeta, null, 2)});

export default APP_META;
`;

  await fs.writeFile(outputPath, source, 'utf8');
  console.log(`Synced app metadata -> js/app-meta.js (${version}, ${buildLabel}, ${commit})`);
}

main().catch((error) => {
  console.error('Failed to sync app metadata:', error);
  process.exitCode = 1;
});
